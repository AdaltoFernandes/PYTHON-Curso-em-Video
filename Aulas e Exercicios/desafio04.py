n = input("Digite a sua palavra ou numero: ")
print('O tipo primitivo', type(n))
print('É numerico?',n.isnumeric())
print('É alfabético? ',n.isalpha())
print('É todo Maiusculo? ', n.isupper())
print('É todo Minisculo? ', n.islower())
print('Contem letra ou numero? ', n.isalnum())
print('Contem só espaços? ', n.isspace())



